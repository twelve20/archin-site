# –ù–∞—Å—Ç—Ä–æ–π–∫–∞ firewall
echo "üîí –ù–∞—Å—Ç—Ä–æ–π–∫–∞ firewall..."
sudo ufw allow 'Nginx Full'
sudo ufw allow OpenSSH
echo "y" | sudo ufw enable

echo "‚úÖ –ë–∞–∑–æ–≤–∞—è –Ω–∞—Å—Ç—Ä–æ–π–∫–∞ –∑–∞–≤–µ—Ä—à–µ–Ω–∞!"
echo ""
echo "üìã –°–ª–µ–¥—É—é—â–∏–µ —à–∞–≥–∏:"
echo "1. –£–±–µ–¥–∏—Ç–µ—Å—å —á—Ç–æ DNS –∑–∞–ø–∏—Å–∏ –¥–æ–º–µ–Ω–∞ —É–∫–∞–∑—ã–≤–∞—é—Ç –Ω–∞ IP —ç—Ç–æ–≥–æ —Å–µ—Ä–≤–µ—Ä–∞"
echo "   - A –∑–∞–ø–∏—Å—å: $DOMAIN -> IP —Å–µ—Ä–≤–µ—Ä–∞"
echo "   - A –∑–∞–ø–∏—Å—å: www.$DOMAIN -> IP —Å–µ—Ä–≤–µ—Ä–∞"
echo "2. –ó–∞–≥—Ä—É–∑–∏—Ç–µ –∫–æ–¥ –ø—Ä–∏–ª–æ–∂–µ–Ω–∏—è –≤ /var/www/archin-site"
echo "3. –ó–∞–ø—É—Å—Ç–∏—Ç–µ —Å–∫—Ä–∏–ø—Ç deploy-app.sh"
echo "4. –ü–æ–ª—É—á–∏—Ç–µ SSL —Å–µ—Ä—Ç–∏—Ñ–∏–∫–∞—Ç –∫–æ–º–∞–Ω–¥–æ–π: sudo certbot --nginx -d $DOMAIN -d www.$DOMAIN"
